#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char *str;
	int i;
	int j;

	i = 0;
	j = ft_strlen((char *)s);
	if (s && f)
	{
		str = (char *)malloc(sizeof(str) * j);
		while (s[i] != '\0')
		{
			str[i] = f(i, s[i]);
			i++;
		}
		return (str);
	}
	return (NULL);
}
