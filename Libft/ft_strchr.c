#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;
	char *p;
	
	i = 0;
	*p = c;
	while(s[i])
	{
		if (s[i] == p[1])
			return (p);
		if (p[1] == '\0')
			return (p);
		i++;
	}
	return (NULL);
}
