#include "libft.h"

int		ft_pow(int x, unsigned int y)
{
	int p;

	if (y == 0)
		return (1);
	p = ft_pow(x, y / 2);
	if (y % 2 == 0)
		return (p * p);
	else
		return (x * p * p);
}
