#include "libft.h"

int		ft_ispunct(int c)
{
	if ((c >= '!' && c <= 39) || (c >= '(' && c <= 47) ||
			(c >= ':' && c <= '?') || (c == '@') || (c >= '[' && c <= '_') ||
			(c == '`') || (c >= '{' && c <= '~'))
		return (1);
	return (0);
}
